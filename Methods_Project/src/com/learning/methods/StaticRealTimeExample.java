package com.learning.methods;

public class StaticRealTimeExample {

	public static void main(String[] args) {
		
		
		System.out.println(Math.sqrt(16.0));
		
		System.out.println(Math.max(Math.PI, 30));
		
		
		int max = Math.max(700, 800);
		
		System.out.println(max);
		
		if(max > 500)
		{
			System.out.println(Math.min(Math.sqrt(max), Math.sqrt(400)));
		}
		

	}

}
